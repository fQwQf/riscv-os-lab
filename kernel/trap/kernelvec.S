/* kernelvec.S — 内核陷阱入口汇编（Lab4 任务2）
 *
 * 当 CPU 处于 S-Mode 并发生中断或异常时，硬件会：
 *   1. 将 PC 保存到 sepc
 *   2. 跳转到 stvec 寄存器指定的地址（即这里的 sys_trap_vector 标签）
 *
 * 本文件的任务：
 *   1. 保存当前所有寄存器到栈上（不能丢失任何状态！）
 *   2. 调用 C 函数 sys_trap_handler() 处理中断原因
 *   3. 从栈上恢复所有寄存器
 *   4. 用 sret 返回到被打断的代码继续执行
 *
 * 注意：保存和恢复的顺序必须完全镜像对称！否则寄存器会"张冠李戴"。
 */

    .globl sys_trap_vector
    .align 4

sys_trap_vector:
    # =============================================================
    # 步骤1：在栈上分配 32*8 = 256 字节的空间
    # 用于保存全部 32 个通用寄存器（每个 64 位 = 8 字节）
    # 注意：x0（zero）永远是 0，但为了统一偏移量，也留一个槽位
    # =============================================================
    addi sp, sp, -256

    # 保存各寄存器到栈（每个 sd 指令存 8 字节）
    # 格式：sd <寄存器>, <从sp的偏移量>(sp)
        # =============================================================
    # TODO [Lab4-任务2-步骤1]：
    #   继续保存剩余寄存器。请严格按照偏移量递增（每次加8）填写：
    #   s0(fp) 在 56(sp)，s1 在 64(sp)，a0 在 72(sp)，a1 在 80(sp)
    #   a2-a7 功能依次在 88~128(sp)，s2-s11 在 136~208(sp)
    #   t3 在 216(sp)，t4 在 224(sp)，t5 在 232(sp)，t6 在 240(sp)
    #   注意：sp 自身（8(sp)）跳过，稍后单独处理
    # =============================================================
    # sd s0,  56(sp)
    # sd s1,  64(sp)
    # ... 补充完整
    # x0/zero 不保存（永远是 0，偏移 0 留空）
    sd ra,   0(sp)      # x1/ra   — 偏移  0
    # x2/sp  — 偏移  8，稍后特殊处理
    sd gp,  16(sp)      # x3/gp   — 偏移 16
    sd tp,  24(sp)      # x4/tp   — 偏移 24
    sd t0,  32(sp)      # x5/t0   — 偏移 32
    sd t1,  40(sp)      # x6/t1   — 偏移 40
    sd t2,  48(sp)      # x7/t2   — 偏移 48
    sd s0,  56(sp)      # x8/s0   — 偏移 56  [任务2-步骤1 补全开始]
    sd s1,  64(sp)      # x9/s1   — 偏移 64
    sd a0,  72(sp)      # x10/a0  — 偏移 72
    sd a1,  80(sp)      # x11/a1  — 偏移 80
    sd a2,  88(sp)      # x12/a2  — 偏移 88
    sd a3,  96(sp)      # x13/a3  — 偏移 96
    sd a4, 104(sp)      # x14/a4  — 偏移104
    sd a5, 112(sp)      # x15/a5  — 偏移112
    sd a6, 120(sp)      # x16/a6  — 偏移120
    sd a7, 128(sp)      # x17/a7  — 偏移128
    sd s2, 136(sp)      # x18/s2  — 偏移136
    sd s3, 144(sp)      # x19/s3  — 偏移144
    sd s4, 152(sp)      # x20/s4  — 偏移152
    sd s5, 160(sp)      # x21/s5  — 偏移160
    sd s6, 168(sp)      # x22/s6  — 偏移168
    sd s7, 176(sp)      # x23/s7  — 偏移176
    sd s8, 184(sp)      # x24/s8  — 偏移184
    sd s9, 192(sp)      # x25/s9  — 偏移192
    sd s10, 200(sp)     # x26/s10 — 偏移200
    sd s11, 208(sp)     # x27/s11 — 偏移208
    sd t3, 216(sp)      # x28/t3  — 偏移216
    sd t4, 224(sp)      # x29/t4  — 偏移224
    sd t5, 232(sp)      # x30/t5  — 偏移232
    sd t6, 240(sp)      # x31/t6  — 偏移240  [任务2-步骤1 补全结束]

    # =============================================================
    # 步骤2：保存 sp。注意：此时 sp 已经被减去了 256，
    #   所以要保存的是"减去 256 之前"的原始 sp 值
    # =============================================================
    addi t0, sp, 256   # t0 = 进入 sys_trap_vector 前的原始 sp 值
    sd   t0, 8(sp)     # 保存原始 sp 到槽位 8(sp)

    # =============================================================
    # 步骤3：所有寄存器现已安全存入栈中，可以调用 C 函数
    # =============================================================
    call sys_trap_handler

    # =============================================================
    # 步骤4：从栈上恢复所有寄存器（顺序必须与保存时完全镜像）
    # 注意：sp 要最后恢复，否则后续 ld 指令的基地址会出错
    # =============================================================
# =============================================================
    # TODO [Lab4-任务2-步骤2]：
    #   用 ld 指令恢复上面保存的所有寄存器，顺序必须与 sd 对称。
    #   最后恢复 sp：必须是最后一步！
    #   如果使用 ld sp, 8(sp) 来恢复 sp，则删除下面这行 addi；
    #   如果不用 ld 恢复 sp，则保留 addi 来恢到栈顶。
    # =============================================================
    ld ra,   0(sp)      # x1/ra
    # x2/sp 最后恢复
    ld gp,  16(sp)      # x3/gp
    ld tp,  24(sp)      # x4/tp   （保存 hartid，故需恢复）
    ld t0,  32(sp)      # x5/t0
    ld t1,  40(sp)      # x6/t1
    ld t2,  48(sp)      # x7/t2
    ld s0,  56(sp)      # x8/s0   [任务2-步骤2 补全开始]
    ld s1,  64(sp)      # x9/s1
    ld a0,  72(sp)      # x10/a0
    ld a1,  80(sp)      # x11/a1
    ld a2,  88(sp)      # x12/a2
    ld a3,  96(sp)      # x13/a3
    ld a4, 104(sp)      # x14/a4
    ld a5, 112(sp)      # x15/a5
    ld a6, 120(sp)      # x16/a6
    ld a7, 128(sp)      # x17/a7
    ld s2, 136(sp)      # x18/s2
    ld s3, 144(sp)      # x19/s3
    ld s4, 152(sp)      # x20/s4
    ld s5, 160(sp)      # x21/s5
    ld s6, 168(sp)      # x22/s6
    ld s7, 176(sp)      # x23/s7
    ld s8, 184(sp)      # x24/s8
    ld s9, 192(sp)      # x25/s9
    ld s10, 200(sp)     # x26/s10
    ld s11, 208(sp)     # x27/s11
    ld t3, 216(sp)      # x28/t3
    ld t4, 224(sp)      # x29/t4
    ld t5, 232(sp)      # x30/t5
    ld t6, 240(sp)      # x31/t6  [任务2-步骤2 补全结束]

    # sp 最后恢复（用保存的原始值）
    ld sp, 8(sp)        # x2/sp 恢复（注意：此后不能再用 sp 作 base）

    # =============================================================
    # 步骤5：sret — 从内核陷阱返回
    #   硬件会自动将 PC 设置为 sepc 寄存器中保存的地址，
    #   恢复中断使能状态（sstatus.SIE），返回被打断的代码继续执行。
    # =============================================================
    sret

# =============================================================
# timervec — M-Mode 时钟中断处理程序（框架提供，无需修改）
#
# 当 CLINT 的 mtime 超过 mtimecmp 时，CPU 跳到这里（M-Mode）。
# 本程序的两项职责：
#   1. 将 mtimecmp 向后推迟 interval，安排下一次中断
#   2. 向 sip.SSIP 写 1，向 S-Mode 注入一个软件中断信号
# 最后用 mret 返回原先被打断的代码（可能是 S-Mode）。
#
# 注意：timervec 工作在 M-Mode，它不能使用内核的普通栈（那是 S-Mode 的）。
#   mscratch 寄存器指向 timer_scratch[]，提供临时存储空间。
# =============================================================
    .globl timervec
    .align 4
timervec:
    # timer_scratch[0..2]：保存 a0/a1/a2 三个临时寄存器
    # timer_scratch[3]   ：CLINT_MTIMECMP(hartid) 地址
    # timer_scratch[4]   ：时钟间隔值
    csrrw a0, mscratch, a0    # a0 = &timer_scratch; mscratch = old a0
    sd a1, 8(a0)
    sd a2, 16(a0)

    # 读当前 mtimecmp，加上 interval，写回（安排下次中断）
    ld a1, 24(a0)             # a1 = CLINT_MTIMECMP 地址
    ld a2, 32(a0)             # a2 = interval
    ld a1, 0(a1)              # a1 = *mtimecmp（当前值）
    add a1, a1, a2            # a1 += interval
    ld a2, 24(a0)             # a2 = CLINT_MTIMECMP 地址（重新加载）
    sd a1, 0(a2)              # *mtimecmp = a1（更新）

    # 向 sip.SSIP 写 1，通知 S-Mode 时钟到了
    li a1, 2                  # SSIP = bit 1
    csrw sip, a1

    # 恢复 a0/a1/a2
    ld a2, 16(a0)
    ld a1, 8(a0)
    csrrw a0, mscratch, a0    # 恢复 a0，mscratch 恢复为 &timer_scratch

    mret
