/* entry.S — 内核引导汇编（Lab1 任务2）
 *
 * 这是内核被 QEMU 加载后执行的第一段代码。
 * CPU 在 0x80000000 处开始无脑执行这里的指令。
 *
 * 你的目标：
 *   1. 在内存中为 C 语言划定一块"栈"区域
 *   2. 设置栈指针寄存器 sp 指向栈顶
 *   3. 跳转到 C 函数 start_main()
 */

    .section .text.entry     /* 告诉链接器：把我放在 .text.entry 节，保证排最前面 */
    .global _entry           /* 声明 _entry 为全局符号，供 kernel.ld 的 ENTRY() 使用 */
    .global stack0           /* 供 start.c 获取栈基地址 */

_entry:
    # =============================================================
    # 每个 hart 分配独立的 4096 字节栈
    # mhartid -> a0，用来选择栈槽位
    # =============================================================
    csrr a0, mhartid       # a0 = 当前 hart 编号
    li   a1, 4096
    addi a0, a0, 1         # hart0 -> offset=1, hart1 -> offset=2 ...
    mul  a0, a0, a1        # a0 = (hartid+1)*4096
    la   sp, stack0
    add  sp, sp, a0        # sp 指向本 hart 的栈顶

    # =============================================================
    # Lab4：先进入 M-Mode 的 start()，再由 mret 降权到 S-Mode
    # =============================================================
    call start

spin:
    # 如果 C 函数意外返回（不应该），进入死循环防止 PC 乱跑
    j spin

    # ------------------------------------------------------------------
    # 下方是预先在内存中划出的 4096 字节栈空间
    # .bss 节：程序加载时自动清零，不占 ELF 文件体积
    # .align 4：按 2^4=16 字节对齐（RISC-V 栈对齐要求）
    # ------------------------------------------------------------------
    .section .bss
    .align 4
stack0:
    .space 4096 * 8   /* 为 8 个 hart 各分配一页栈空间 */
